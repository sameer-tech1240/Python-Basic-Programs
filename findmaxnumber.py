def maximum(num1, num2):
    return max(num1, num2)

num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))
maximum = maximum(num1, num2)
print(f"The maximum of {num1} and {num2} is: {maximum}")
 
